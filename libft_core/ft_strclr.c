#include "libft.h"

void	ft_strclr(char *s)
{
	size_t	len;
	char	*str;

	if (s)
	{
		str = (char*)s;
		len = ft_strlen(s) - 1;
		while (str != (str + len))
		{
			*(str + len) = '\0';
			--len;
		}
		*str = '\0';
	}
}
