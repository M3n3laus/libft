#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*temp;

	temp = s;
	i = 0;
	while (i < n)
	{
		*(temp + i++) = c;
	}
	return (s);
}
