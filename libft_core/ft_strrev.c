#include "libft.h"

void	ft_strrev(char *s)
{
	int i;
	int j;

	i = 0;
	j = ft_strlen(s) - 1;
	while ((i != j) != (i > j))
	{
		ft_chrswap(s + i, s + j);
		i++;
		j--;
	}
}
